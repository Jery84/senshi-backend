DROP TABLE IF EXISTS TBL_CLUB;

CREATE TABLE TBL_CLUB (
  ID INT AUTO_INCREMENT  PRIMARY KEY,
  NAME VARCHAR(250) NOT NULL
);

DROP TABLE IF EXISTS TBL_CATEGORY;

CREATE TABLE TBL_CATEGORY (
  CD_CATEGORY VARCHAR(25)  PRIMARY KEY,
  NAME VARCHAR(250) NOT NULL,
  YEAR_MIN INT NOT NULL,
  YEAR_MAX INT NOT NULL
);

CREATE TABLE TBL_JUDOKA (
  ID INT AUTO_INCREMENT  PRIMARY KEY,
  LICENSE VARCHAR(50) ,
  FIRST_NAME VARCHAR(250) NOT NULL,
  LAST_NAME VARCHAR(250) NOT NULL,
  DATE_OF_BIRTH DATE NULL,
  WEIGHT FLOAT NULL,
  GENDER CHAR (6) NOT NULL,
  FL_PRESENT CHAR(1) NOT NULL,
  CD_CATEGORY VARCHAR(25) NULL,
  ID_CLUB INT NOT NULL
);

 ALTER TABLE TBL_JUDOKA
    ADD FOREIGN KEY (CD_CATEGORY)
    REFERENCES TBL_CATEGORY(CD_CATEGORY);

 ALTER TABLE TBL_JUDOKA
    ADD FOREIGN KEY (ID_CLUB)
    REFERENCES TBL_CLUB(ID);